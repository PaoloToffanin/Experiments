function options = sos_instructions(options, language)

switch language 
    case 'English'
        options.instructions.start = ['We want to test your understanding of ',...
					'speech in the presence of another speaker.\n'...
          'The target sentence you have to repeat will start half a second ',...
          'after the masking speech.\n'...
          'The background speech will be made of chopped up words that should ',...
          'not make much sense.\n'...
          'Your task is to repeat the target sentence. Your spoken responses ',...
          'will be recorded for further analyses.\n\n'...
          '-------------------------------------------------\n\n'];
        
        options.instructions.training1 = ['You will now listen to ONLY the ',...
					'target speaker \n', ...
          'just to get acquainted with how he/she sounds like.\n Please repeat',...
          'the sentence.\n\n', ...
          '-------------------------------------------------\n\n'];
        
        options.instructions.training2 = ['You will now listen to samples of ',...
					'BOTH the target speaker AND \n', ...
          'the masker just to get used to the task.\n Please repeat the target ',...
          'sentence.\n\n', ...
          '-------------------------------------------------\n\n'];
        options.instructions.test = ['You will now begin the actual test. The ',...
					'target sentence you have to repeat \n', ...
          'will start half a second after the masking speech. The background ',...
          'speech will be made of chopped up words ', ...
          'that should not make much sense. Your task is to repeat the target ',...
          'sentence.\nYour spoken responses will be recorded for further analyses.\n', ...
            '-------------------------------------------------\n\n'];
        
        options.instructions.vocoded = ['All the following sounds will now be ',...
        'a simulation of what\n'...
        'Cochlear Implant subjects hear. For this reason, all sounds will be ',...
        'very distorted.\n\n'...
        '-------------------------------------------------\n\n'];
        
        options.instructions.listen = ['Listen carefully to the target sentence.\n\n'...
            '-------------------------------------------------\n\n'];
        
        options.instructions.repeat = ['Now repeat the target sentence.\n\n'...
            '-------------------------------------------------\n\n'];
        
        options.instructions.feedback = ['This is the correct sentence.\n\n'...
            '-------------------------------------------------\n'];
        
        options.instructions.end = ['Congratulations!! This session is over. '...
        'Thanks for participating.\n\n'...
        '-------------------------------------------------\n'];
        
        options.instructions.breaktime = ['And now it''s time for a 5min break. '...
        'Please leave everything as is. When you''re\n', ...
        'ready to proceed, please click the ''CONTINUE'' button below.\n\n', ...
            '-------------------------------------------------\n'];
    case 'Dutch'
        options.instructions.start = ['Wij willen uw begrip van spraak in', ... 
        'de aanwezigheid van een andere spreker testen. De doelzin die u moet ', ...
     'herhalen zal een halve seconde na de maskerende spreker beginnen. ', ...
     'De achtergrondconversatie bestaat uit geknipte ', ... 
     'woorden die niet moeten kloppen. Uw taak is om de doelzin te herhalen. ', ... 
     'Uw gesproken antwoorden worden opgenomen voor ', ...
     'verdere analyse.\n', ...
     '-------------------------------------------------\n\n'];
				options.instructions.training1 = ['U zult nu luisteren naar alleen de ', ...
				'doelspreker om kennis te maken met de manier waarop hij/zij klinkt.\n', ...
						'Herhaal alstublieft de zin.\n'...
						'-------------------------------------------------\n\n'];
				options.instructions.training2 = ['U zult nu luisteren naar voorbeelden', ... 
				'van ZOWEL de beoogde spreker EN de maskerende spreker om te wennen aan de taak.\n', ...
						'Alstublieft herhaal de doelzin.\n\n', ...
						'-------------------------------------------------\n\n'];
				options.instructions.test = ['U zult nu beginnen met de werkelijke test. ', ...
				'De doelzin die u moet herhalen zal een halve seconde na de maskerende ', ...
				'spreker beginnen. De achtergrondconversatie bestaat uit geknipte woorden ', ...
				'die niet moeten kloppen. Uw taak is om de', ...
						'doelzin te herhalen. Uw gesproken antwoorden worden opgenomen voor ', ...
						'verdere analyse.\n', ...
						'-------------------------------------------------\n\n'];
				options.instructions.listen = ['Luister goed naar de doelzin.\n', ...
						'-------------------------------------------------\n\n'];
				options.instructions.repeat = ['Herhaal alstublieft de zin.\n', ...
						'-------------------------------------------------\n\n'];
				options.instructions.feedback = ['Dit is de correcte zin.\n', ...
						'-------------------------------------------------\n\n'];
				options.instructions.end = ['Gefeliciteerd!! Deze sessie is voorbij. ', ...
				'Dank u voor uw deelname.\n', ...
						'-------------------------------------------------\n\n'];
				options.instructions.breaktime = ['En nu is het tijd voor 5 minuten pauze. ', ...
				'Laat alles achter zoals het is.\n', ...
						'Wanneer u klaar bent om verder te gaan, kunt u hieronder klikken op de ', ...
						'knop "DOORGAAN".\n', ...
						'-------------------------------------------------\n\n'];

    otherwise
        fprintf('I do not recognize the language %s\n use english instead', language);
end